def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # 동해선 route_id 조회
    cur.execute("SELECT id FROM routes WHERE code=?", (DONGHAE_CODE,))
    row = cur.fetchone()
    if not row:
        print(f"ERROR: 노선 코드 '{DONGHAE_CODE}' 를 찾을 수 없습니다.")
        conn.close()
        return
    route_id = row[0]
    print(f"동해선 route_id={route_id}")

    inserted = 0
    skipped = 0

    for name, km, lat, lon in STATIONS:
        # 중복 확인 (route_id + name)
        cur.execute(
            "SELECT id FROM facilities WHERE route_id=? AND name=?",
            (route_id, name)
        )
        if cur.fetchone():
            print(f"  SKIP (이미 존재): {name} {km}km")
            skipped += 1
            continue

        cur.execute(
            "INSERT INTO facilities (route_id, type, name, km, has_station_map, lat, lon) "
            "VALUES (?, 'GENERAL_STATION', ?, ?, 0, ?, ?)",
            (route_id, name, km, lat, lon)
        )
        print(f"  INSERT: {name} {km}km ({lat}, {lon})")
        inserted += 1

    conn.commit()
    conn.close()
    print(f"\n완료: {inserted}건 삽입, {skipped}건 건너뜀")


if __name__ == "__main__":
    main()
