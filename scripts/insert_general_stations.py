def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    inserted = 0
    skipped = 0
    for route_id, name, km, lat, lon in STATIONS:
        # 이미 동일 노선·이름 존재하면 skip
        cur.execute(
            "SELECT id FROM facilities WHERE route_id=? AND name=?",
            (route_id, name)
        )
        if cur.fetchone():
            skipped += 1
            continue
        cur.execute(
            """INSERT INTO facilities
               (route_id, type, name, km, has_station_map, lat, lon)
               VALUES (?, 'GENERAL_STATION', ?, ?, 0, ?, ?)""",
            (route_id, name, km, lat, lon)
        )
        inserted += 1

    conn.commit()
    conn.close()
    print(f"완료: {inserted}건 삽입, {skipped}건 중복 skip")


if __name__ == "__main__":
    main()
