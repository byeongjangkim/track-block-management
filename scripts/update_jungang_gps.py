def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    cur.execute("SELECT id FROM routes WHERE code='jungang'")
    route_id = cur.fetchone()[0]

    updated = skipped = not_found = 0
    for name, km, lat, lon in rows:
        if lat is None:
            skipped += 1
            continue
        cur.execute(
            "UPDATE facilities SET lat=?, lon=? WHERE route_id=? AND name=? AND km=?",
            (lat, lon, route_id, name, km)
        )
        if cur.rowcount:
            updated += 1
        else:
            print(f"  NOT FOUND: {name} {km}km")
            not_found += 1

    conn.commit()
    conn.close()
    print(f"완료: {updated}건 업데이트, {skipped}건 건너뜀(좌표없음), {not_found}건 미매칭")

if __name__ == "__main__":
    main()
